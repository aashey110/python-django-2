#ARITHMETIC OPERATORS
num1 = 10
num2 = 20

sum = num1 + num2    #ADDITION
print(sum)  # Output: 30

difference = num1 - num2    #SUBTRACTION
print(difference)  # Output: -10

product = num1 * num2   #MULTIPLICATION
print(product)  # Output: 200

quotient = num2 / num1      #DIVISION
print(quotient)  # Output: 2.0

Remainder = num1 / num2     #Moudulus DIVISION
print(Remainder)  # Output: 0.0


